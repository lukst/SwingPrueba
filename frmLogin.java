    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        ConexionJavaOracle conexion = new ConexionJavaOracle();
        conexion.conectarDB();
        String usuario = txtUsuario.getText().toUpperCase();
        String pass = new String(txtClave.getPassword());
        if (!conexion.obtenerValoresPorCampo("USUARIOS", "USUARIO").equals(usuario)
                && conexion.obtenerValoresPorCampo("USUARIOS", "password").equals(pass)) {
            JOptionPane.showMessageDialog(rootPane, "Usuario o clave incorrecto");
            txtUsuario.setText("");
            txtClave.setText("");
            txtUsuario.requestFocusInWindow();
            return;
        }
        frmPrincipal miPrincipal = new frmPrincipal();
        this.setVisible(false);
        miPrincipal.setExtendedState(JFrame.MAXIMIZED_BOTH);
        miPrincipal.setVisible(true);
    }//GEN-LAST:event_btnAceptarActionPerformed

